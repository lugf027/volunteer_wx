DROP TABLE USER;
CREATE TABLE USER(
    USER_ID VARCHAR(36) NOT NULL   COMMENT '主键ID 本表主键ID' ,
    USER_NAME VARCHAR(36)    COMMENT '登录账户' ,
    USER_PWD VARCHAR(36)    COMMENT '登录密码' ,
    USER_NICKNAME VARCHAR(36)    COMMENT '用户昵称' ,
    USER_OPEN_ID VARCHAR(36)    COMMENT '微信openId' ,
    USER_GENDER INT    COMMENT '用户性别' ,
    USER_AGE INT    COMMENT '用户年龄' ,
    USER_CAREER VARCHAR(36)    COMMENT '用户职业' ,
    USER_TEL VARCHAR(36)    COMMENT '用户手机号码' ,
    USER_ADDR VARCHAR(128)    COMMENT '用户地址' ,
    USER_AVATAR_URL VARCHAR(255)    COMMENT '微信头像' ,
    USER_IF_BANNED VARCHAR(1)    COMMENT '是否被禁用' ,
    USER_IF_ORGAN VARCHAR(1)    COMMENT '招募资质' ,
    USER_IF_SUPER VARCHAR(1)    COMMENT '运维身份' ,
    USER_REG_TIME VARCHAR(36)    COMMENT '注册时间' ,
    USER_LONGITUDE DECIMAL(32,10)    COMMENT '登陆经度' ,
    USER_LATITUDE DECIMAL(32,10)    COMMENT '登陆纬度' ,
    SESSION_KEY VARCHAR(36)    COMMENT '登录状态码' ,
    PRIMARY KEY (USER_ID)
) COMMENT = '用户表 ';

ALTER TABLE USER COMMENT '用户表';
DROP TABLE ORGANIZATION;
