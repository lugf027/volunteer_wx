ALTER TABLE ORGANIZATION COMMENT '组织机构表';
DROP TABLE ACTIVITY;
CREATE TABLE ACTIVITY(
    ACT_ID VARCHAR(36) NOT NULL   COMMENT '主键ID 本表主键ID' ,
    USER_ID VARCHAR(36)    COMMENT '发起者ID' ,
    ACT_NAME VARCHAR(36)    COMMENT '活动名称' ,
    ACT_ADDR VARCHAR(36)    COMMENT '活动地址' ,
    ACT_REC_BEGIN VARCHAR(36)    COMMENT '申请开始时间' ,
    ACT_REC_END VARCHAR(36)    COMMENT '申请截止时间' ,
    ACT_TIME_BEGIN VARCHAR(36)    COMMENT '活动开始时间' ,
    ACT_TIME_END VARCHAR(36)    COMMENT '活动截止时间' ,
    ACT_USER_NUM VARCHAR(36)    COMMENT '需求人数' ,
    ACT_DEMANDS VARCHAR(1024)    COMMENT '活动内容与要求' ,
    ACT_STATUS VARCHAR(36)    COMMENT '活动状态 0：已提交；1：招募中；2：招募成功待开始；3：招募失败待处理；4：已取消；5：进行中；6：已结束' ,
    CHECK_LONGITUDE DECIMAL(32,10)    COMMENT '签到经度' ,
    CHECK_LATITUDE DECIMAL(32,10)    COMMENT '签到纬度' ,
    PRIMARY KEY (ACT_ID)
) COMMENT = '志愿活动表 ';

ALTER TABLE ACTIVITY COMMENT '志愿活动表';
