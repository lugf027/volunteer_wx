    ORGAN_ADDR VARCHAR(36)    COMMENT '组织地址' ,
    ORGAN_TEL VARCHAR(36)    COMMENT '组织电话' ,
    ORGAN_INTRO VARCHAR(36)    COMMENT '组织简介' ,
    ORGAN_STATUS VARCHAR(36)    COMMENT '组织申请状态 0：提交申请待审核；1：通过；2：失败' ,
    PRIMARY KEY (ORGAN_ID)
